    private void tfUsuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfUsuarioKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (tfUsuario.getText().equalsIgnoreCase("Zark")) {
                tfContra.setEnabled(true);
                tfUsuario.setEnabled(false);
                JOptionPane.showMessageDialog(null, "Hola Zark. Escribe tu Contraseña");
            }else{
                JOptionPane.showMessageDialog(null, "Usuario no Encontrado");
                tfUsuario.setText("");
                
            }
        }
    }//GEN-LAST:event_tfUsuarioKeyPressed

    private void tfContraKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfContraKeyPressed
        
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            
            if (tfContra.getText().equalsIgnoreCase("1234")) {
                
                Icon foto = new ImageIcon(getClass().getResource("/img/camilo.jpeg"));
                Object nivel = JOptionPane.showInputDialog(rootPane, "Cual es el Nivel del Usuario?","Nivel del Usuario", JOptionPane.QUESTION_MESSAGE, foto,
                new Object[]{"Administrador", "Usuario Comun"},"Administrador");
                JOptionPane.showMessageDialog(rootPane, nivel);
                //JOptionPane.showMessageDialog(null, "Bienenido Zark.");
                tfContra.setEnabled(false);
                
                ImageIcon pFoto = new ImageIcon(getClass().getResource("/img/camilo.jpeg"));
                ImageIcon icono = new ImageIcon(pFoto.getImage().getScaledInstance(lblFoto.getWidth(), lblFoto.getHeight(),Image.SCALE_DEFAULT));
                lblFoto.setIcon(icono);

                Thread hilos = new Thread(){
                    @Override
                    public void run(){
                        for (int i = 0; i <= 100; i++) {
                            try {
                                Thread.sleep(10);
                                pbar1.setValue(i);
                                if (pbar1.getValue() == 100) {
                                    dispose();
                                    java.awt.EventQueue.invokeLater(new Runnable() {
                                        public void run() {
                                            new MdiPrincipal().setVisible(true);
                                        }
                                    });

                                }
                            } catch (InterruptedException ex) {
                                Logger.getLogger(logIn.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                    }
                };
                hilos.start();
                
                
            }else{
                JOptionPane.showMessageDialog(null, "No es tu Contraseña");
                tfContra.setText("");
            }
            
            
            
            
            
        }
    }//GEN-LAST:event_tfContraKeyPressed

