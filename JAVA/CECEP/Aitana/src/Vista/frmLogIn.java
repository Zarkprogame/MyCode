    private void txtUsuarioKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtUsuarioKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtUsuario.getText().equalsIgnoreCase("Aitana")) {
                txtContra.setEnabled(true);
                txtUsuario.setEnabled(false);
            }else {
                JOptionPane.showMessageDialog(null, "No se encuentra al Usuario");
                txtUsuario.setText("");
            }
        }
    }//GEN-LAST:event_txtUsuarioKeyPressed

    private void txtContraKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtContraKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (txtContra.getText().equals("0000")) {
                
                Icon foto = new ImageIcon(getClass().getResource("/img/usuario.png"));
                Object nivelUsuario = JOptionPane.showInputDialog(rootPane, "Cual es el Nivel del Usuario?","Nivel del Usuario", JOptionPane.QUESTION_MESSAGE, foto,
                new Object[]{"Administrador", "Usuario"},"Administrador");
                txtContra.setEnabled(false);
                
                ImageIcon pfoto = new ImageIcon(getClass().getResource("/img/usuario.png"));
                ImageIcon icono = new ImageIcon(pfoto.getImage().getScaledInstance(lblFoto.getWidth(), lblFoto.getHeight(), Image.SCALE_DEFAULT));
                lblFoto.setIcon(icono);
                
                Thread hilo = new Thread(){
                    public void run(){
                        for (int i = 0; i <= 100; i++) {
                            try {
                                Thread.sleep(10);
                                ProgressBar.setValue(i);
                                if (ProgressBar.getValue() == 100) {
                                    dispose();
                                    
                                    String zapatos = JOptionPane.showInputDialog("Digite Cantidad de Registros:");
                                    cantZap = Integer.parseInt(zapatos);
//                                    setTitle("Tipo de Zapato");
//                                    
                                    java.awt.EventQueue.invokeLater(new Runnable() {
                                        public void run() {
                                            new formzapatos().setVisible(true);
                                        }
                                    });
                                    
                                } 
                            } catch (InterruptedException ex) {
                                Logger.getLogger(frmLogIn.class.getName()).log(Level.SEVERE, null, ex);
                            }
                        }
                    }
                };
                hilo.start();
                
                
            }else {
                JOptionPane.showMessageDialog(null, "No es la Contrase単a");
                txtContra.setText("");
            }
        }
    }//GEN-LAST:event_txtContraKeyPressed

